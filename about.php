          <h3>The Advantages of playing Chess</h3>
          <h4>Sparks your creativity</h4>
          <img src="img/creativity.jpg">
          <p>Since the right hemisphere of the brain is responsible for creativity, it should come as no surprise that activating the right side of your brain helps develop your creative side. Specifically, chess greatly increases originality. One four-year study had students from grades 7 to 9 play chess, use computers, or do other activities once a week for 32 weeks to see which activity fostered the most growth in creative thinking. The chess group scored higher in all measures of creativity, with originality being their biggest area of gain.</p>
          <h4>Increases problem-solving skills</h4>
          <img src="img/solving.jpg">
          <p>A chess match is like one big puzzle that needs solving, and solving on the fly, because your opponent is constantly changing the parameters. Nearly 450 fifth-grade students were split into three groups in a 1992 study in New Brunswick. Group A was the control group and went through the traditional math curriculum. Group B supplemented the math with chess instruction after first grade, and Group C began the chess in first grade. On a standardized test, Group C’s grades went up to 81.2% from 62% and outpaced Group A by 21.46%.</p>
          <h4>Improves your memory</h4>
          <img src="img/memory.jpg">
          <p>Chess players know — as an anecdote — that playing chess improves your memory. Being a good player means remembering how your opponent has operated in the past and recalling moves that have helped you win before. But there’s hard evidence also. In a two-year study in 1985, young students who were given regular opportunities to play chess improved their grades in all subjects, and their teachers noticed better memory and better organizational skills in the kids. A similar study of Pennsylvania sixth-graders found similar results. Students who had never before played chess improved their memories and verbal skills after playing.</p>
          <h4>It can raise your IQ</h4>
          <img src="img/iq.jpg">
          <p>Chess has always had an image problem, being seen as a game for brainiacs and people with already high IQs. So there has been a bit of a chicken-and-egg situation: do smart people gravitate towards chess, or does playing chess make them smart? At least one study has shown that moving those knights and rooks around can in fact raise a person’s intelligence quotient. A study of 4,000 Venezuelan students produced significant rises in the IQ scores of both boys and girls after 4 months of chess instruction.</p>
          <h4>Improves concentration</h4>
          <img src="img/concentration.jpg">
          <p>Chess masters might come off like scattered nutty professors, but the truth is their antics during games are usually the result of intense concentration that the game demands and improves in its players. Looking away or thinking about something else for even a moment can result in the loss of a match, as an opponent is not required to tell you how he moved if you didn’t pay attention. Numerous studies of students in the U.S., Russia, China, and elsewhere have proven time and again that young people’s ability to focus is sharpened with chess.</p>
